namespace BudgetManager.ManagerWeb.Models.SettingModels
{
    /// <summary>
    /// Represents the API URLs used in the application.
    /// </summary>
    public class ApiUrls
    {
        /// <summary>
        /// Gets or sets the URL for the main API.
        /// </summary>
        public string MainApi { get; set; }

        /// <summary>
        /// Gets or sets the URL for the authentication API.
        /// </summary>
        public string AuthApi { get; set; }

        /// <summary>
        /// Gets or sets the URL for the financial API.
        /// </summary>
        public string FinApi { get; set; }
    }
}
